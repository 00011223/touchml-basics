    mongodb它是文档型数据库！它的数据存储形式是一种二进制格式的，类似于JSON数据的一种对象格式！

    mongodb真实是采用BSON格式进行存储数据！


    总结：mongodb本质是就是一个存储数据的‘仓库’！

    数据存储在数据库中，可以使得读写操作更快！有优化！查询更方便！以及对数据进行统一的管理！


    为什么采用mongodb数据库？
    mongodb数据库它是支持JavaScript脚本语言！其次它的存储方式是web开发中最喜欢和常用的类似于json的数据格式！
