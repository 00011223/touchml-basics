/* 
    npm(node packgage manager) node的包管理工具

    什么是npm？
    官网：https://www.npmjs.com/

    npm是通过互联网进行第三方的包下载、上传、更新等操作的工具！

    npm简单理解，就是个代码仓库（代码超市），你想使用什么包（模块）就从‘仓库’中下载即可！前提是必须知道包的名字！

    怎么下载npm？
    npm不需要下载，它是和nodejs一起绑定的！当安装nodejs时就已经安装了npm工具了！

    npm常用的命令：
        npm init 初始化一个nodejs项目，会生成一个package.json依赖文件
        npm install 包名 下载一个具体的包到本地项目中！简写形式为： npm i 包名

        npm install 包名 -g 以全局的方式下载包到本地中！

        npm uninstall 包名  卸载一个包！

        npm run 脚本命令  运行package.json中的脚本命令
*/