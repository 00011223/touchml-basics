    服务器归根结底是提供服务的“计算机”,为互联网中不同的客户端提供服务的

    服务器根据其服务应用的不同，类别也是不同的：
    
    常见的服务器种类：
        - 文件服务器
        - 域名服务器
        - 邮件服务器
        - web服务器
        - 数据库服务器
    我们重点学习是web服务器！


    web服务器：也称作为网站服务器，响应客户端发送的请求，并返回对应的网页资源，这些资源可以是任何资源，文本、文件、图片、音频等
    总结：web服务器就是给用户提供网上信息以及各种资源的应用服务！








